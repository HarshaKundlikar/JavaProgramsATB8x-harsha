package Sept_one;

public class Lab010_Do_and_while {
    public static void main(String[] args) {
        int i=1;
        do{
            for(int j=5;j>=0;j--) {
                System.out.println("*"+j);
            }
            i++;
        }while (i==5);

        System.out.println("star");
        int w=1;
        while(w>=0)
        {
            for(i=1;i<=5;i++)
            {
                System.out.println("*"+i);
            }
            w++;
        }

    }
}
